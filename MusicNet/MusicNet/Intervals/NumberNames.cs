using System;
using System.Collections.Generic;
using System.Text;

namespace REMuns.Music.Intervals
{
    /// <summary>
    /// Represents a name of the number of a simple perfectable interval.
    /// </summary>
    public enum PSimpleIntervalNumberName
    {
        /// <summary>
        /// Represents a unison.
        /// </summary>
        Unison = 1,

        /// <summary>
        /// Represents a fourth.
        /// </summary>
        Fourth = 4,

        /// <summary>
        /// Represents a fifth.
        /// </summary>
        Fifth = 5,
    }

    /// <summary>
    /// Represents a name of the number of a simple non-perfectable interval.
    /// </summary>
    public enum NPSimpleIntervalNumberName
    {
        /// <summary>
        /// Represents a second.
        /// </summary>
        Second = 2,

        /// <summary>
        /// Represents a third.
        /// </summary>
        Third = 3,

        /// <summary>
        /// Represents a sixth.
        /// </summary>
        Sixth = 6,

        /// <summary>
        /// Represents a seventh.
        /// </summary>
        Seventh = 7,
    }
}
